#include <bits/stdc++.h>
using namespace std;
typedef long long ll;
int main()
{
    int gb;
    cin >> gb;
    ll mb = gb * 1000;
    ll kb = gb * 1000000;
    ll byte = gb * 1000000000;
    cout << "Megabyte = " << mb << endl;
    cout << "Kilobyte = " << kb << endl;
    cout << "Byte = " << byte << endl;
    return 0;
}